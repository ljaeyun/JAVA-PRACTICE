package sec14;

public class Board 
{
	private int bno;  //숫자
	private String bti;  //제목
	private String bcont; //내용
	private String bwri; //작성자
	
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getBti() {
		return bti;
	}
	public void setBti(String bti) {
		this.bti = bti;
	}
	public String getBcont() {
		return bcont;
	}
	public void setBcont(String bcont) {
		this.bcont = bcont;
	}
	public String getBwri() {
		return bwri;
	}
	public void setBwri(String bwri) {
		this.bwri = bwri;
	}
	
	
	
}
